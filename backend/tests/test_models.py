class TestArtifactRoundTrip:
    def test_dump_and_validate(self):
        art = Artifact(
            library_id="lib-1",
            kind="idea",
            goal="prove transducer hypothesis",
            constraints=[{"type": "word_limit", "value": 4000}],
            project_ids=["p1"],
            material_ids=["m1", "m2"],
            title="Transducer hypothesis",
        )
        data = art.model_dump()
        restored = Artifact.model_validate(data)
        assert restored.kind == "idea"
        assert restored.goal == art.goal
        assert restored.constraints == [{"type": "word_limit", "value": 4000}]
        assert restored.project_ids == ["p1"]
        assert restored.material_ids == ["m1", "m2"]
        assert restored.title == "Transducer hypothesis"


