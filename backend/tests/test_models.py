# ── IDs are auto-generated valid UUIDs ────────────────────────────────


class TestAutoGeneratedIds:
    def test_library_id(self):
        assert _valid_uuid(Library(name="x").id)

    def test_project_id(self):
        assert _valid_uuid(Project(library_id="x", goal="g").id)

    def test_conversation_id(self):
        assert _valid_uuid(Conversation(library_id="x").id)

    def test_claim_id(self):
        assert _valid_uuid(Claim(library_id="x", body="b").id)

    def test_material_id(self):
        assert _valid_uuid(Material(library_id="x", kind="paper").id)

    def test_artifact_id(self):
        assert _valid_uuid(Artifact(library_id="x", kind="idea", goal="g").id)

    def test_ids_are_unique(self):
        ids = [Library(name="x").id for _ in range(100)]
        assert len(set(ids)) == 100


# ── Datetimes are auto-populated ──────────────────────────────────────


class TestAutoDatetimes:
    def test_library_created_at(self):
        lib = Library(name="x")
        assert isinstance(lib.created_at, datetime)

    def test_conversation_timestamps(self):
        conv = Conversation(library_id="x")
        assert isinstance(conv.created_at, datetime)
        assert isinstance(conv.updated_at, datetime)

    def test_claim_timestamps(self):
        claim = Claim(library_id="x", body="b")
        assert isinstance(claim.created_at, datetime)
        assert isinstance(claim.updated_at, datetime)

    def test_artifact_timestamps(self):
        art = Artifact(library_id="x", kind="idea", goal="g")
        assert isinstance(art.created_at, datetime)
        assert isinstance(art.updated_at, datetime)
