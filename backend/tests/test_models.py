class TestMaterialRoundTrip:
    def test_dump_and_validate(self):
        mat = Material(
            library_id="lib-1",
            kind="paper",
            provenance={"doi": "10.1234/example"},
            payload={"abstract": "..."},
        )
        data = mat.model_dump()
        restored = Material.model_validate(data)
